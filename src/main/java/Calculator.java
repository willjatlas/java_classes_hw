public class Calculator {

    // Add
    public int add(int x, int y) { return x + y; }

    // Subtract
    public int subtract(int x, int y) { return x - y; }

    // Multiply
    public int multiply(int x, int y) { return x * y; }

    // Divide
    public double divide(double x, double y) { return x / y; }

}
